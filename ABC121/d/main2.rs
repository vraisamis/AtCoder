fn xor(l: u64, r: u64) -> u64 {
    if l > r {
        println!("reverse! l:{}, r:{}", l, r);
        xor(r, l)
    } else if l == 0 {
        match r % 4 {
            0 => r,
            1 => 1,
            2 => r + 1,
            3 => 0,
            _ => 9999
        }
    } else {
        xor(0, l - 1) ^ xor(0, r)
    }
}
